extern int current_slot;

#define PIN_DIN 11
#define PIN_CLK 10
#define PIN_CS 9
#define PIN_DC 8
#define PIN_RST 12

static uint8_t fb[64][16];
static char current_status[32] = "Booting...";
static uint8_t battery_level = 0;
static bool battery_charging = false;
static bool battery_valid = false;

static void send_cmd(uint8_t cmd) {
    gpio_put(PIN_DC, 0);
    gpio_put(PIN_CS, 0);
    spi_write_blocking(spi1, &cmd, 1);
    gpio_put(PIN_CS, 1);
}

static void send_data(uint8_t data) {
    gpio_put(PIN_DC, 1);
    gpio_put(PIN_CS, 0);
    spi_write_blocking(spi1, &data, 1);
    gpio_put(PIN_CS, 1);
}

void oled_init() {
    spi_init(spi1, 10000 * 1000); // 10MHz
    gpio_set_function(PIN_CLK, GPIO_FUNC_SPI);
    gpio_set_function(PIN_DIN, GPIO_FUNC_SPI);
    
    gpio_init(PIN_CS);
    gpio_set_dir(PIN_CS, GPIO_OUT);
    gpio_put(PIN_CS, 1);
    
    gpio_init(PIN_DC);
    gpio_set_dir(PIN_DC, GPIO_OUT);
    gpio_put(PIN_DC, 0);
    
    gpio_init(PIN_RST);
    gpio_set_dir(PIN_RST, GPIO_OUT);

    gpio_init(15);
    gpio_set_dir(15, GPIO_IN);
    gpio_pull_up(15);
    gpio_init(17);
    gpio_set_dir(17, GPIO_IN);
    gpio_pull_up(17);
    
    gpio_put(PIN_RST, 1);
    sleep_ms(100);
    gpio_put(PIN_RST, 0);
    sleep_ms(100);
    gpio_put(PIN_RST, 1);
    sleep_ms(100);

    send_cmd(0xae);
    send_cmd(0x00);
    send_cmd(0x10);
    send_cmd(0xB0);
    send_cmd(0xdc);
    send_cmd(0x00);
    send_cmd(0x81);
    send_cmd(0x6f);
    send_cmd(0x21);
    send_cmd(0xa0);
    send_cmd(0xc0);
    send_cmd(0xa4);
    send_cmd(0xa6);
    send_cmd(0xa8);
    send_cmd(0x3f);
    send_cmd(0xd3);
    send_cmd(0x60);
    send_cmd(0xd5);
    send_cmd(0x41);
    send_cmd(0xd9);
    send_cmd(0x22);
    send_cmd(0xdb);
    send_cmd(0x35);
    send_cmd(0xad);
    send_cmd(0x8a);
    
    sleep_ms(200);
    send_cmd(0xaf);

    oled_clear();
    oled_update();
}

void oled_clear() {
    memset(fb, 0, sizeof(fb));
}

void draw_pixel(int x, int y, bool color) {
    if (x < 0 || x >= 64 || y < 0 || y >= 128) return;
    int py = x;
    int px = 127 - y;
    if (color) {
        fb[py][px / 8] |= (1 << (px % 8));
    } else {
        fb[py][px / 8] &= ~(1 << (px % 8));
    }
}

// Draw Row-Major 16-bit wide bitmap with variable height
void draw_bitmap_v(int x, int y, const uint16_t* bitmap, int h) {
    for (int row = 0; row < h; row++) {
        uint16_t line = bitmap[row];
        for (int col = 0; col < 16; col++) {
            if (line & (1 << (15 - col))) {
                draw_pixel(x + col, y + row, true);
            }
        }
    }
}

