void draw_char(int x, int y, char c) {
    if (c < 32 || c > 126) c = '?';
    int font_idx = c - 32;
    for (int col = 0; col < 5; col++) {
        uint8_t line = font5x7[font_idx][col];
        for (int row = 0; row < 8; row++) {
            if (line & (1 << row)) {
                draw_pixel(x + col, y + row, true);
            }
        }
    }
}

void oled_print(int x, int y, const char* text) {
    int start_x = x;
    while (*text) {
        if (*text == '\n') {
            x = start_x;
            y += 9;
        } else {
            draw_char(x, y, *text);
            x += 6; 
            if (x + 5 >= 64) {
                x = start_x;
                y += 9;
            }
        }
        text++;
    }
}

void oled_update() {
    for (int y = 0; y < 64; y++) {
        send_cmd(0xb0);
        int column = 63 - y;
        send_cmd(0x00 + (column & 0x0f));
        send_cmd(0x10 + (column >> 4));
        for (int x_page = 0; x_page < 16; x_page++) {
            send_data(fb[y][x_page]);
        }
    }
}

void oled_set_status(const char* status) {
    strncpy(current_status, status, sizeof(current_status) - 1);
    current_status[sizeof(current_status) - 1] = '\0';
    
    oled_clear();
    
    // 1. Header: Chain (16x10) + Slot Num
    static const uint16_t icon_chain[] = {
        0x3E00, 0x4100, 0x4100, 0x4F7C, 0x4882, 0x3882, 0x077C, 0x0400, 0x0400, 0x03E0
    };
    draw_bitmap_v(12, 4, icon_chain, 10);
    char slot_num[4];
    snprintf(slot_num, sizeof(slot_num), "%d", current_slot + 1);
    oled_print(32, 6, slot_num);
    
    // 2. Status text
    oled_print(4, 25, current_status);

    // 3. Waiting State: Hourglass (12x16)
    if (strcmp(current_status, "Waiting...") == 0) {
        static const uint16_t icon_hourglass[] = {
            0xFFF0, 0x8010, 0x8010, 0x4020, 0x7FE0, 0x3FC0, 0x1F80, 0x0F00, 
            0x0F00, 0x1F80, 0x2040, 0x4020, 0x4020, 0x8010, 0x8010, 0xFFF0
        };
        draw_bitmap_v(26, 45, icon_hourglass, 16);
    }

    // 4. Battery Info
    if (battery_valid && strcmp(current_status, "Connected") == 0) {
        // Large Battery Frame (24x12)
        for(int i=0; i<20; i++) {
            draw_pixel(10+i, 45, true);
            draw_pixel(10+i, 56, true);
        }
        for(int i=0; i<12; i++) {
            draw_pixel(10, 45+i, true);
            draw_pixel(30, 45+i, true);
        }
        draw_pixel(31, 48, true); draw_pixel(31, 49, true); draw_pixel(31, 50, true); draw_pixel(31, 51, true); draw_pixel(31, 52, true);

        if (battery_charging) {
            oled_print(36, 47, "CHG");
        } else {
            int fill_w = (battery_level * 18) / 8;
            if (fill_w > 18) fill_w = 18;
            for(int i=0; i<fill_w; i++) {
                for(int j=0; j<8; j++) {
                    draw_pixel(11+i, 47+j, true);
                }
            }
            char pct_str[8];
            int pct = (battery_level * 100) / 8;
            if (pct > 100) pct = 100;
            snprintf(pct_str, sizeof(pct_str), "%d%%", pct);
            oled_print(36, 47, pct_str);
        }
    }

    // 5. Soft Keys
    // Opposite Arrows (Away pointing) (16x13)
    static const uint16_t icon_next[] = {
        0x1000, 0x3000, 0x7000, 0xFF00, 0x7000, 0x3000, 0x1000, 0x0008, 0x000C, 0x000E, 0x00FF, 0x000E, 0x000C, 0x0008
    };
    draw_bitmap_v(8, 110, icon_next, 14);
    
    // Trash Can (12x14)
    static const uint16_t icon_trash[] = {
        0x3C00, 0x7E00, 0xFF00, 0x8100, 0xA500, 0xA500, 0xA500, 0xA500, 0xA500, 0xA500, 0xA500, 0xA500, 0x8100, 0x7E00
    };
    draw_bitmap_v(40, 110, icon_trash, 14);

    oled_update();
}

void oled_set_battery(uint8_t level, bool charging) {
    static uint8_t last_level = 255;
    static bool last_charging = false;
    
    if (level != last_level || charging != last_charging || !battery_valid) {
        battery_level = level;
        battery_charging = charging;
        battery_valid = true;
        last_level = level;
        last_charging = charging;
        oled_set_status(current_status);
    }
}
